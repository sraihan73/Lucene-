shared_ptr<DocData> SortableSingleDocSource::getNextDocData(
    shared_ptr<DocData> docData) 
{
  docData = SingleDocSource::getNextDocData(docData);
  shared_ptr<Properties> props = make_shared<Properties>();

  // random int
  // C++ TODO: There is no native C++ equivalent to 'toString':
  props->put(L"sort_field", Integer::toString(r->nextInt(sortRange)));

  // random string
  int len = nextInt(2, 20);
  std::deque<wchar_t> buffer(len);
  for (int i = 0; i < len; i++) {
    buffer[i] = static_cast<wchar_t>(r->nextInt(0x80));
  }
  props->put(L"random_string", wstring(buffer));

  // random country
  props->put(L"country", COUNTRIES[r->nextInt(COUNTRIES.size())]);
  docData->setProps(props);
  return docData;
}

int SortableSingleDocSource::nextInt(int start, int end)
{
  return start + r->nextInt(end - start);
}

void SortableSingleDocSource::setConfig(shared_ptr<Config> config)
{
  SingleDocSource::setConfig(config);
  sortRange = config->get(L"sort.rng", 20000);
  r = make_shared<Random>(config->get(L"rand.seed", 13));
}
